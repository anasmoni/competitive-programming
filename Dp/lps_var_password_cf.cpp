///https://codeforces.com/contest/126/problem/B

int main(){

    string pat;
    cin>>pat;
    int M = pat.size();
    int lps[M+1] , len=0, i=1;

    lps[0] = 0;

    while (i < M)
    {
        if (pat[i] == pat[len])
        {
            len++;
            lps[i] = len;
            i++;
        }
        else
        {
            if (len != 0)
            {
                len = lps[len-1];
            }
            else
            {
                lps[i] = 0;
                i++;
            }
        }
    }
    int mx=0;

    for(int i=1; i < (M-1) ; i++) mx = max(mx,lps[i]);
    int l = lps[M-1];
    while(mx<l) l = lps[l-1];

    if(l==0)cout<<"Just a legend";
    else cout<<pat.substr(0,l);
    return 0;
}
